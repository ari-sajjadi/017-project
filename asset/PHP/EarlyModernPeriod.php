          href="https://en.wikipedia.org/wiki/Indies" class="mw-redirect" title="Indies">Indies</a>, he took <a
          href="https://en.wikipedia.org/wiki/Goa" title="Goa">Goa</a> in India in 1510, and <a
          href="https://en.wikipedia.org/wiki/Malacca" title="Malacca">Malacca</a> on the <a
          href="https://en.wikipedia.org/wiki/Malay_peninsula" class="mw-redirect" title="Malay peninsula">Malay
          peninsula</a> in 1511. The Portuguese could now trade directly with <a
          href="https://en.wikipedia.org/wiki/Thailand" title="Thailand">Siam</a>, <a
          href="https://en.wikipedia.org/wiki/China" title="China">China</a>, and the <a
