<h1 class="viewport"><span class="scroll-link-bg"></span>Early Modern Period</h1>
      <p class="pointer-slideshow columns-3"><a href="https://en.wikipedia.org/wiki/Spain" title="Spain">Spain</a> and
        <a href="https://en.wikipedia.org/wiki/Portugal" title="Portugal">Portugal</a> were interested in seeking new
        routes to trade in spices and other valuable products from Asia. The control of trade routes and the
        spice-producing regions were the main reasons that <a href="https://en.wikipedia.org/wiki/Portugal"
          title="Portugal">Portuguese</a> navigator <a href="https://en.wikipedia.org/wiki/Vasco_da_Gama"
          title="Vasco da Gama">Vasco da Gama</a> sailed to <a href="https://en.wikipedia.org/wiki/India"
          title="India">India</a> in 1499.<a href="https://en.wikipedia.org/wiki/Spice#cite_note-8"
            title="Spice"></a>When <a href="https://en.wikipedia.org/wiki/Vasco_da_Gama"
          title="Vasco da Gama">Gama</a> discovered the pepper market in <a href="https://en.wikipedia.org/wiki/India"
          title="India">India</a>, he was able to secure peppers for a much cheaper price than the ones demanded by <a
          href="https://en.wikipedia.org/wiki/Venice" title="Venice">Venice</a>.<a href="#cite_note-:0-9"></a> At around the same time, <a
          href="https://en.wikipedia.org/wiki/Christopher_Columbus" title="Christopher Columbus">Christopher
          Columbus</a> returned from the <a href="https://en.wikipedia.org/wiki/New_World" title="New World">New
          World</a>. He described to <a href="https://en.wikipedia.org/wiki/Investor" title="Investor">investors</a> new
        spices available there.<a href="https://en.wikipedia.org/wiki/Wikipedia:Citation_needed"
              title="Wikipedia:Citation needed">citation needed]</a>
      </p>
      <p class="pointer-slideshow columns-3">Another source of competition in the spice trade during the 15th and 16th
        century was the <a href="https://en.wikipedia.org/wiki/Republic_of_Ragusa"
          title="Republic of Ragusa">Ragusans</a> from the maritime republic of <a
          href="https://en.wikipedia.org/wiki/Dubrovnik" title="Dubrovnik">Dubrovnik</a> in southern Croatia.<a href="#cite_note-11"></a></p>
      <p class="pointer-slideshow columns-3">The military prowess of <a
          href="https://en.wikipedia.org/wiki/Afonso_de_Albuquerque" title="Afonso de Albuquerque">Afonso de
          Albuquerque</a> (1453â€“1515) allowed the Portuguese to take control of the sea routes to India. In 1506, he
        took the island of <a href="https://en.wikipedia.org/wiki/Socotra" title="Socotra">Socotra</a> in the mouth of
        the <a href="https://en.wikipedia.org/wiki/Red_Sea" title="Red Sea">Red Sea</a> and, in 1507, <a
          href="https://en.wikipedia.org/wiki/Ormuz" class="mw-redirect" title="Ormuz">Ormuz</a> in the <a
