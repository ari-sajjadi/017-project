<h1 class="viewport">Spice</h1>
      <p class="pointer-slideshow columns-3">For the sensation of eating spicy-hot foods, see pungency. For other uses,
        see Spice <a href="https://en.wikipedia.org/wiki/Spice_(disambiguation)">(disambiguation).</a>
      </p>

      <p class="pointer-slideshow columns-3">A <b>spice</b> is a 
      <a href="https://en.wikipedia.org/wiki/Seed" title="Seed">seed</a>, 
          <a href="https://en.wikipedia.org/wiki/Fruit" title="Fruit">fruit</a>, 
          <a href="https://en.wikipedia.org/wiki/Root" title="Root">root</a>, 
          <a href="https://en.wikipedia.org/wiki/Bark_(botany)" title="Bark (botany)">bark</a>, or other 
          <a href="https://en.wikipedia.org/wiki/Plant" title="Plant">plant</a> substance primarily used for 
          <a href="https://en.wikipedia.org/wiki/Flavor" title="Flavor">flavoring</a>, 
          <a href="https://en.wikipedia.org/wiki/Color" title="Color">coloring</a> or 
          <a href="https://en.wikipedia.org/wiki/Preservative" title="Preservative">preserving</a> food. Spices are
        distinguished from 
        <a href="https://en.wikipedia.org/wiki/Herb" title="Herb">herbs</a>, which are the leaves,
        flowers, or stems of plants used for 
        <a href="https://en.wikipedia.org/wiki/Flavor" title="Flavor">flavoring</a>
        or as a 
        <a href="https://en.wikipedia.org/wiki/Garnish_(food)" title="Garnish (food)">garnish</a>. Many spices
        have 
        <a href="https://en.wikipedia.org/wiki/Antimicrobial" title="Antimicrobial">antimicrobial</a> properties.
        This may explain why spices are more commonly used in warmer climates, which have more 
        <a href="https://en.wikipedia.org/wiki/Infectious_disease" class="mw-redirect"
          title="Infectious disease">infectious diseases</a>, and why the use of spices is prominent in meat, which is
        particularly susceptible to spoiling. Spices are sometimes used in 
        <a href="https://en.wikipedia.org/wiki/Medicine" title="Medicine">medicine</a>, 
          <a href="https://en.wikipedia.org/wiki/Sacred_rite" class="mw-redirect" title="Sacred rite">religious
          rituals</a>, 
          <a href="https://en.wikipedia.org/wiki/Cosmetics" title="Cosmetics">cosmetics</a> or 
          <a href="https://en.wikipedia.org/wiki/Perfume" title="Perfume">perfume</a> production.
      </p>